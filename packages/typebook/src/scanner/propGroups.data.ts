import type { PropGroup } from "../types";

// Standard attribute groups, keyed by name. Grounded in the specs mirrored by `@types/react`:
// WAI-ARIA (aria), WHATWG global attributes (global), per-element content attributes (element),
// HTML microdata / RDFa, SVG, and the DOM/React event categories (from each `on*` handler type).
// The name lists are extracted from `@types/react`'s interfaces; classification is by NAME so a
// manually-declared attribute (not inherited from React) is grouped the same as an inherited one.

/** Event base name → category (from the `*EventHandler` type each `on*` uses in `DOMAttributes`). */
